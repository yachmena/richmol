!  potential for fitting upper Varandas pes
      SUBROUTINE potv(npoints, V,R1,R2,theta)

      integer, intent(in) :: npoints
      double precision, intent(in) :: R1(npoints), R2(npoints), theta(npoints)
      double precision, intent(out) :: V(npoints)
      double precision :: VQ(npoints), Vd(npoints), Vv(npoints)
      integer :: i

! VD   is the MVD2 CORRECTION IN A.U
! VQ   is the QED CORRECTION IN A.U

      do i=1, npoints
        CALL PESleq6(VQ(i),R1(i),R2(i),theta(i))
        CALL pesd2x(Vd(i),R1(i),R2(i),theta(i))
        call poten(Vv(i),R1(i),R2(i),theta(i))
      enddo

       v=Vv/219474.634d0 + VQ + Vd 

end 




